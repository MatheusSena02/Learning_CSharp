using System;

namespace MetodosComRetorno
{
    public class Calculadora()
    {
        public int Somar(int x, int y)
        {
            return x + y;
        }
        public int Subtrair(int x, int y)
        {
            return x - y;
        }
        public int Produto(int x, int y)
        {
            return x * y;
        }
        public int Quociente(int x, int y)
        {
            return x / y;
        }
        public int Modul(int x, int y)
        {
            return x % y;
        }




    }
}
